public class OperadoresRelacionais {

    //Operador de igualdade | Verifica se dois números são iguais
    public boolean igual(int a, int b) {
        return a == b;
    }

    //Operador de diferenca | Verifica se dois números sao diferentes
    public boolean diferente(int a, int b) {
        return a != b;
    }

    //Operado de maior que | Verifica se o primeiro numero é maior que o segundo
    public boolean maiorQue(int a, int b) {
        return a > b;
    }

    //Operador de menor que | Verifica se o primeiro numero é menor que o segundo
    public boolean menorQue(int a, int b) {
        return a < b;
    }

    //Operado de maior ou igual | Verifica se o primeiro numero é maior ou igual o segundo
    public boolean maiorOuigual(int a, int b) {
        return a >= b;
    }

    //Operador de menor ou igual | Verifica se o primeiro numero é menor ou igual o segundo
    public boolean menorOuigual(int a, int b) {
        return a <= b;
    }


}